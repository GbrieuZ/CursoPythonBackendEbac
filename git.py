# git init      Inicializa um novo repositório Git.
# git clone <url>       Clona um repositório remoto para o local.
# git status    Mostra o estado atual do repositório.
# git add <arquivo>     Adiciona um arquivo à área de preparação (staging area).
# git add .     Adiciona todos os arquivos alterados à área de preparação.
# git commit -m "msg"   Faz o commit (registra) as alterações preparadas com uma mensagem.
# git log       Mostra o histórico de commits.
# git log --oneline     Mostra um histórico de commits conciso.
# git diff      Mostra a diferença entre as alterações.
# git diff <arquivo>    Mostra as alterações para um arquivo específico.

# git branch            Lista todas as branches (ramos).
# git branch <nome-da-branch>   Cria uma nova branch.
# git checkout <nome-da-branch> Alterna para uma branch.
# git checkout -b <nome>        Cria e alterna para uma nova branch.
# git merge <nome-da-branch>    Mescla (funde) uma branch na atual.
# git branch -d <nome>  Exclui uma branch.
# git branch -D <nome>  Força a exclusão de uma branch.